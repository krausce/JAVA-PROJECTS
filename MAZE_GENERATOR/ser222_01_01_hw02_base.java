    private static void makeMazeRecursive(char[][]level, int startX, int startY, int endX, int endY)
    {
         int vertWallLoc = ((endX - startX)/2);
         int horizWallLoc = ((endY - startY)/2);

         if ((endX - startX) > 3)
         {
             // Draw new vertical wall
            for (int y = startY; y < endY; y++)
                level[y][vertWallLoc-1] = ICON_WALL;
         }
         if ((endY - startY) > 3) 
         {
            // Draw new horizontal wall
            for (int x = startX; x < endX; x++)
                level[horizWallLoc-1][x] = ICON_WALL;
         }

         else if (((endX - startX) < 3) && ((endY - startY) < 3))
         {
             return;
         }
         makeMazeRecursive(level, startX, startY, vertWallLoc, horizWallLoc);
         makeMazeRecursive(level, vertWallLoc, startY, endX, horizWallLoc);
         makeMazeRecursive(level, startX, horizWallLoc, vertWallLoc, endY);
         makeMazeRecursive(level, vertWallLoc, horizWallLoc, endX, endY);
        
    }
    
